                TransferFunction("Aufgabe S","4/(s^2 - 4)"); ...
                TransferFunction("Aufgabe T","-1000*(s+2)^2/(4*(s+1)^3*(s+10))"); ...
                TransferFunction("Aufgabe U","2*s/(s^2 + 2*s + 1)") ...
            ];
            end
    end
end